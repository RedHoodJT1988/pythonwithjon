# There are three different types of numbers
# in Python:
# 1. Int which stands for integer 1, 2, 5, 155, 3250
# 2. Float which stands for floating point number 1 .2, 3.5 , 42.3
# 3. Complex This above the scope of this course.

# These are Ints
x = 3
y = 47474747
z = 0

# These are Floats
a = 2.3
b = 45.6
c = 7879.9

# This is a complex number
w = 42j
e = 350j

# The decimal number type could be a 4th type. The decimal type
# has a few added benefits that float doesn't. You can find more
# information here: https://docs.python.org/3/library/decimal.html

# Addition(+) and subtraction(-) examples
print("Add and subtract examples")
print(1 + 3)
print(1 + 3.5)
print(6 - 8)
print(-1.1 + 5)
print("\n")

# Multiplication(*) examples
print("Multiplication examples")
print(3 * 3)
print(10 * 100)
print(1000 * 10000)
print("\n")

# Division(/) examples
print("Division examples")
print(3 / 2)
print(10 / 5)
print(200 / 15)
print("\n")

# Division(//) examples
print("Division floor examples")
print(3 // 2)
print(10 // 5)
print(200 // 15)
print("\n")
